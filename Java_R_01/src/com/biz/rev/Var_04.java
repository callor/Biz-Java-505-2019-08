package com.biz.rev;

public class Var_04 {

	public static void main(String[] args) {

		// 정수형 변수 num를 선언하고
		// 3과 4를 곱셈한 후
		// 그 결과를 변수 num에 보관하라
		int num = 3 * 4;
		
		// 앞에 선언된 변수 num 에
		// 4를 2로 나눈 결과를 보관하라
		// 만약 num 변수가 앞에서 선언되지 않으면 오류 발생!!
		num = 4/2;
		
		String s = "KOREA";
		
		// 앞에서 선언된 변수 s에
		// 3가지 문자열을 연결하여 저장하라
		s = "Republic" + "of" + "Korea";
		// s에 저장된 값?
		// RepublicofKorea
		
		
		// 앞에서 선언된 변수 s에
		// 문자열 30+40 을 저장하라
		s = "30 + 40";
		// s에 저장된 값?
		// 30 + 40
		
	}
}
