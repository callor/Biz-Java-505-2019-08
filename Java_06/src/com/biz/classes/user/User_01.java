package com.biz.classes.user;

public class User_01 {

	// 임의로 생성한 add() 메서드
	public static void add() {

		/*
		 * num1과 num2 두개의 int 형 변수를 선언하고
		 * num1 변수에는 30을 num2 변수에는 40을 할당(저장)한다
		 * 
		 * int 형 sum 변수를 선언하고
		 * num1과 num2 변수를 덧셈하여 결과를 sum에 저장한다.
		 * = 변수 num1, 변수 num2에 저장되어 있는 값을 읽어서
		 * 		덧셈을 수행한후 결과를 sum에 저장한다.
		 * 
		 * 덧셈 결과가 담겨있는 sum 변수의 내용을
		 * 콘솔에 보여라
		 */
		int num1 = 30;
		int num2 = 40;
		int sum = num1 + num2;
		System.out.println(sum);
		
	}
}
