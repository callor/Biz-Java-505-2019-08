package com.biz.var;

public class IntVar_03 {

	public static void main(String[] args) {
		
		int num1 = 0;
		
		num1 = 30 + 40;
		System.out.println(num1); // 70
		
		// Modula 연산
		// 나머지 연산
		num1 = 30 % 4;
		System.out.println(num1);
		
		num1 = 57;
		System.out.println("57을 3으로 나눈 몫 : ");
		System.out.println(57 / 3);
		
		System.out.println("57을 3으로 나눈 나머지 : ");
		System.out.println(57 % 3);
		
	}

}
