package com.biz.var;

public class Print_01 {

	public static void main(String[] args) {

		System.out.println("대한민국만세");
		System.out.println(); // 빈줄표시
		System.out.println("우리나라만세");
		System.out.print(30+40);
		System.out.print(30-40);
		System.out.println();
		
		int num1 = 30;
		int num2 = 7;
		
		System.out.print(num1);
		System.out.print(" / ");
		System.out.print(num2);
		System.out.print(" = ");
		System.out.println(30/7);

		System.out.print(num1);
		System.out.print(" % ");
		System.out.print(num2);
		System.out.print(" = ");
		System.out.println(30%7);

		
	}

}
