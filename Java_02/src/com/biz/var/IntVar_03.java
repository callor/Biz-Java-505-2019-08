package com.biz.var;




public class IntVar_03 {
	// method : 일종의 개발자가 만든 keyword
	//		명령문들의 모음
	// main(String[] args)
	// main()
	public static void main(String[] args) {
		// 1. num1 변수를 선언, 30을 저장
		int num1 = 30; 
		
		// 2. num1 변수(에 저장된 값)를 
		//		읽어서 콘솔에 표시
		System.out.println(num1);
		
		// 3. 모든 변수 소멸
	}
	// 4. 현재 코드 실행 종료
}
