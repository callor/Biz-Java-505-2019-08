public class For_01 {

	public static void main(String[] args) {

		// 어떤값이 0 ~ (10-1)까지 1씩증가한다
		// 몇개인가?
		// 그 개수만큼 for() {  } 코드를 반복하라
		for(int i = 0 ; i < 10 ; i++) {
			
		}
		
		int num = 100;
		// 앞에서 선언되고 값이 저장된 num 변수를 참조하여
		// 어떤값이 0 ~ (num저장된값 - 1)까지 
		// 1씩 증가한다.
		// 몇개인가?
		// 그 개수만큼 for() {  } 코드를 반복하라
		for(int i = 0 ; i < num ; i++) {
			
		}
		for(int index = 0 ; index < num ; index++) {
			
		}
		for(int n = 0 ; n < num ; n++) {
			
		}
		
		// 변수 x 값이 0부터 (num-1)까지 1씩 증가하고 있다.
		// 변수 x의 변화되는 값을 console에 보여달라
		for(int x = 0; x < num ; x++) {
			System.out.println(x);
		}
		
	}

}
