package com.biz.first;

public class Var_01 {

	public static void main(String[] args) {
		
		// 변수 선언문
		// 정수값을 저장할수 있는 기억장소를 마련하고
		// 그곳에 num1 이라는 이름을 붙여달라
		// 그리고 그곳에 30을 저장해 달라
		int num1 = 30;
		
		int num2 = 40;
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num2 / num1);
		System.out.println(num2 - num1);
	}
}
